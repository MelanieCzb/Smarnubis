		<article>
			Le SNMARTPHNU est crée le 4 avril 1970 : Syndicat National des Médecins AnesthésistesRéanimateurs
			Temps Plein des Hôpitaux non Universitaires.
			Ses buts initiaux sont de défendre les nominations des Anesthésistes-Réanimateurs travaillant en CH non universitaires
			(CH puis CHG- Centre Hospitalier Général) lors des Commissions Partiaires qui étaient composées d’anesthésistes et de chirurgiens. Ce sont bien les chirurgiens qui ont nommé seuls (!) les premiers chefs de service d’anesthésie ;
			de créer dans les Centres Hospitaliers Généraux des postes de Médecins Anesthésistes Réanimateurs Temps Plein, comme dans les CHU, et ne plus se contenter de Médecins à Temps Partiel, partagés entre une « clinique » privée, et un Hôpital- « hospice ». Le SNMARTPHNU veut la fin de l’ anesthésie « foraine » ;
			de créer des Services d’Anesthésie à part entière, comprenant au moins deux postes. Les Anesthésistes de CHG sont pionnier du CHG moderne et leur rôle quotidien est considérable, où ils sont souvent les seuls temps plein de l’Hôpital, assurent tous les gestes d’urgence de la dialyse péritonéale à l’entraînement électro-systolique. L’hôpital « la nuit, nous appartient ». Ils sont les acteurs actifs de la médicalisation des hôpitaux pendant les « trente glorieuses ».
			Sur une scène peuplée de nombreux syndicats isolés, le SMARTPHNU obtient pour les CHG satisfaction sur ses objectifs de création, et assoit l’anesthésiste réanimateur comme responsable des services d’anesthésie, des réanimations, puis des SAMU pour lutter contre la mortalité routière, alors effroyable. 
		</article>
		<article> 
			Il se bat en outre pour : <br>
			- La reconnaissance et la valorisation du travail en « garde » avec le fameux mot d’ordre de J.C
			Courtine : travail de jour = travail de nuit !) <br>
			- La limitation des obligations de service de 11 à 10 ½ journées, obtenue en 1981 <br>
			- L’amélioration de la « sécurité anesthésique », d’où participation des membres du bureau du SMARTPHNU à la Commission Nationale d’Anesthésie, puis à la rédaction du Rapport sur la sécurité anesthésique <br>
			- l’autonomisation de l’anesthésie réanimation de la chirurgie dans les Commissions Nationales Paritaires, et dans l’organisation hospitalière (services d’AR avec Chefs de Service AR) <br>
			- l’amélioration des rémunérations (salaire, retraite) <br>
			- l’autonomisation de la formation en Anesthésie réanimation qui est longtemps considérée comme une spécialité médicale (réforme de l’internat) <br>
			- La création d’un corps spécifiques d’Infirmières Spécialisées, qui deviendra le corps des ISAR puis IADE, avec ouverture de la première école spécialisée à St Germain sous l’impulsion de Marcelle Cara-Beurton <br>
			- la réforme de l’organisation hospitalière mettant en évidence la contrainte de la permanence des soins
		</article>

		<article> 
			En 1987, transformation du SNMARTPNU en SNMARHNU (Syndicat National des
			Médecins Anesthésistes-Réanimateurs des Hôpitaux non Universitaires.)
			Une nouvelle équipe dirigera, à des permutations près, le syndicat pendant 10 ans. Le Conseil d’Administration traduit la diversité géographique et des secteurs d’intervention de la spécialité
			en Centre Hôpital Général (anesthésie, réanimation, urgence, SAMU).
			Ses objectifs sont : <br>
			- la structuration de la profession en milieu hospitalier général (charge de travail, équipes suffisantes), <br>
			- passage au temps plein généralisé, <br>
			- sécurité de l’anesthésie et formation médicale continue, <br>
			- et comme toujours : reconnaissance de la spécificité du travail de nuit et exigence de ce qu’on appelle à l’époque « repos physiologique », travail de nuit = travail de jour, <br>
			- améliorations de la rémunération pour améliorer une attractivité de l’hôpital public qui décline à partir de 1995, et de la « garde » obtenue en 1992. <br>
			Naissance le 30 mars 1992 de la Confédération des Hôpitaux Généraux (CHG), dont le SNMARHNU est un des trois membres fondateurs avec le Syndicat National des Biologistes des Hôpitaux, et la Fédération Mallard, pour faire valoir les droits et la spécificité des Centres Hospitaliers Généraux (50 % des soins MCO en France) différents de ceux des CHU. Signature par son président, JC Ducreux du protocole d’accord du 13 mars 2000, négocié entre les Confédérations et une DH dirigée par Edouard Couty et Danièle Toupillier, qui introduit : l’indemnité pour exercice sur plusieurs établissements, la prime pour exercice public exclusif, les postes à recrutement prioritaire, la nouvelle grille de rémunération (décrets et arrêtés des 8 juin 2000, 17 et 23 octobre 2001).
		</article>

		<article>
			En Septembre 2000, Jean-Claude Ducreux passe la main à Michel Vignier, et le 2
			décembre 2005, le SNMARHNU devient le SMARNU (Syndicat des médecins Anesthésistes
			réanimateurs non universitaires) entérinant des modifications sociologiques et statutaires
			spécifiques
			Signature le 22 Octobre 2001, du Cadrage National, et signé entre B. Kouchner et la totalité des
			Confédérations, qui introduit le repos de sécurité appelé « repos quotidien » après une période de 24 h, la
			durée maximale de travail maximum de 48 heures hebdomadaire lissée sur 4 mois, les RTT et CET.
			Réorientation de l’orientation du syndicat, qui va devenir plus proche des préoccupations concrètes,
			quotidiennes, réelles des PH anesthésistes réanimateurs. L’ « homme » prendra plus d’importance, plus
			que la « structure » dans laquelle il évolue et il faut de recentrer sur les bases :
			• L’organisation du temps de travail
			• L’amélioration de la qualité de vie,
			• La reconnaissance de l’activité en continu : travail de jour = travail de nuit.
			Participation aux Commissions mises en place aux échelons régional et national.
			Début de la résistance aux mesures de « modernisation » de l’hôpital public, dans le sens de l’hôpital
			« entreprise » dans un contexte de crise des comptes sociaux :
			- révolution du financement des hôpitaux <br>
			- révolution de la gouvernance des hôpitaux <br>
			- révolution du statut des médecins à l’hôpital, en particulier du statut du Praticien Hospitalier <br>
			Le 2 octobre 2006, le SMARNU change de président, Michel Vignier passe la main à James Brodeur.
		</article>
		
