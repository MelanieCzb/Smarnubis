<h1>Histoire du Smarnu</h1>

	<section>
		
		<article>
			<p><span>Le SNMARTPHNU</span> est crée le 4 avril 1970 : Syndicat National des Médecins AnesthésistesRéanimateurs
			Temps Plein des Hôpitaux non Universitaires.
			Ses buts initiaux sont de :</p>
			<ul>
				<li>Défendre les nominations des Anesthésistes-Réanimateurs travaillant en CH non universitaires
			(CH puis CHG- Centre Hospitalier Général) lors des Commissions Partiaires qui étaient composées d’anesthésistes et de chirurgiens. Ce sont bien les chirurgiens qui ont nommé seuls (!) les premiers chefs de service d’anesthésie</li>
				<li>De créer dans les Centres Hospitaliers Généraux des postes de Médecins Anesthésistes Réanimateurs Temps Plein, comme dans les CHU, et ne plus se contenter de Médecins à Temps Partiel, partagés entre une « clinique » privée, et un Hôpital- « hospice ». Le SNMARTPHNU veut la fin de l’ anesthésie « foraine »</li>
				<li>De créer des Services d’Anesthésie à part entière, comprenant au moins deux postes. Les Anesthésistes de CHG sont pionnier du CHG moderne et leur rôle quotidien est considérable, où ils sont souvent les seuls temps plein de l’Hôpital, assurent tous les gestes d’urgence de la dialyse péritonéale à l’entraînement électro-systolique. L’hôpital « la nuit, nous appartient ». Ils sont les acteurs actifs de la médicalisation des hôpitaux pendant les « trente glorieuses ».</li>
			</ul>
			<p>Sur une scène peuplée de nombreux syndicats isolés, le <span>SMARTPHNU</span> obtient pour les CHG satisfaction sur ses objectifs de création, et assoit l’anesthésiste réanimateur comme responsable des services d’anesthésie, des réanimations, puis des SAMU pour lutter contre la mortalité routière, alors effroyable. </p>	
		</article>

		<article> 
			<p>Il se bat en outre pour :</p>
			<ul>
				<li>La reconnaissance et la valorisation du travail en « garde » avec le fameux mot d’ordre de J.C
			Courtine : travail de jour = travail de nuit !) </li>
				<li>La limitation des obligations de service de 11 à 10 ½ journées, obtenue en 1981</li>
				<li>L’amélioration de la « sécurité anesthésique », d’où participation des membres du bureau du SMARTPHNU à la Commission Nationale d’Anesthésie, puis à la rédaction du Rapport sur la sécurité anesthésique</li>
				<li>L’autonomisation de l’anesthésie réanimation de la chirurgie dans les Commissions Nationales Paritaires, et dans l’organisation hospitalière (services d’AR avec Chefs de Service AR)</li>
				<li>L’amélioration des rémunérations (salaire, retraite)</li>
				<li>L’autonomisation de la formation en Anesthésie réanimation qui est longtemps considérée comme une spécialité médicale (réforme de l’internat)</li>
				<li>La création d’un corps spécifiques d’Infirmières Spécialisées, qui deviendra le corps des ISAR puis IADE, avec ouverture de la première école spécialisée à St Germain sous l’impulsion de Marcelle Cara-Beurton</li>
				<li>la réforme de l’organisation hospitalière mettant en évidence la contrainte de la permanence des soins</li>
			</ul>
		</article>

		<article>
			<p>En 1987, transformation du SNMARTPNU en <span>SNMARHNU (Syndicat National des
			Médecins Anesthésistes-Réanimateurs des Hôpitaux non Universitaires).</span>
			Une nouvelle équipe dirigera, à des permutations près, le syndicat pendant 10 ans. </p>
			<p>Le Conseil d’Administration traduit la diversité géographique et des secteurs d’intervention de la spécialité
			en Centre Hôpital Général (anesthésie, réanimation, urgence, SAMU).
			Ses objectifs sont :</p>
			<ul>
				<li>La structuration de la profession en milieu hospitalier général (charge de travail, équipes suffisantes),</li>
				<li>Passage au temps plein généralisé,</li>
				<li>Sécurité de l’anesthésie et formation médicale continue,</li>
				<li>Et comme toujours : reconnaissance de la spécificité du travail de nuit et exigence de ce qu’on appelle à l’époque « repos physiologique », travail de nuit = travail de jour,</li>
				<li>Améliorations de la rémunération pour améliorer une attractivité de l’hôpital public qui décline à partir de 1995, et de la « garde » obtenue en 1992.</li>
			</ul>
			<p>Naissance le 30 mars 1992 de la Confédération des Hôpitaux Généraux (CHG), dont le SNMARHNU est un des trois membres fondateurs avec le Syndicat National des Biologistes des Hôpitaux, et la Fédération Mallard, pour faire valoir les droits et la spécificité des Centres Hospitaliers Généraux (50 % des soins MCO en France) différents de ceux des CHU. Signature par son président, JC Ducreux du protocole d’accord du 13 mars 2000, négocié entre les Confédérations et une DH dirigée par Edouard Couty et Danièle Toupillier, qui introduit : l’indemnité pour exercice sur plusieurs établissements, la prime pour exercice public exclusif, les postes à recrutement prioritaire, la nouvelle grille de rémunération (décrets et arrêtés des 8 juin 2000, 17 et 23 octobre 2001).</p>	
		</article>

		<article>
			<p>En Septembre 2000, Jean-Claude Ducreux passe la main à Michel Vignier, et le 2
			décembre 2005, le SNMARHNU devient le <span>SMARNU (Syndicat des médecins Anesthésistes
			réanimateurs non universitaires)</span> entérinant des modifications sociologiques et statutaires
			spécifiques</p>
			<p>Signature le 22 Octobre 2001, du Cadrage National, et signé entre B. Kouchner et la totalité des
			Confédérations, qui introduit le repos de sécurité appelé « repos quotidien » après une période de 24 h, la
			durée maximale de travail maximum de 48 heures hebdomadaire lissée sur 4 mois, les RTT et CET.</p>
			<p>Réorientation de l’orientation du syndicat, qui va devenir plus proche des préoccupations concrètes,
			quotidiennes, réelles des PH anesthésistes réanimateurs. L’ « homme » prendra plus d’importance, plus
			que la « structure » dans laquelle il évolue et il faut de recentrer sur les bases :</p>
			<ul>
				<li>L’organisation du temps de travail</li>
				<li>L’amélioration de la qualité de vie,</li>
				<li>La reconnaissance de l’activité en continu : travail de jour = travail de nuit.</li>
			</ul>
			<p>Participation aux Commissions mises en place aux échelons régional et national.
			Début de la résistance aux mesures de « modernisation » de l’hôpital public, dans le sens de l’hôpital
			« entreprise » dans un contexte de crise des comptes sociaux :</p>
			<ul>
				<li>Révolution du financement des hôpitaux</li>
				<li>Révolution de la gouvernance des hôpitaux</li>
				<li>Révolution du statut des médecins à l’hôpital, en particulier du statut du Praticien Hospitalier 
			Le 2 octobre 2006, le SMARNU change de président, Michel Vignier passe la main à James Brodeur.</li>
			</ul>
		</article>
		
		<article>
			<p>Cette nouvelle présidence aura tout de suite trois objectifs :</p>
				<ul>
					<p>Lutter contre les notions de profit à l’hôpital :</p>
					<li>Structurer le refus des Part Complémentaires Variable (PCV) qui sont programmées pour devenir une part substantielle des émoluments des PH, et ce en construisant une opposition durable basée sur de nouveaux acteurs, dépasser le refus de la PCV en faisant prendre conscience à ces acteurs de leur rôle et intérêts communs dans le cadre de leur position particulière dans le processus de soins à l’hôpital, celui
					de la permanence des soins hospitalière, formaliser cette opposition en une Fédération de la Permanence des Soins Hospitalière avec le
					SNPHAR, l’AMUF, SMU DE France et le Symhospriv (décembre 2006, statuts mars 2008);</li>
				</ul>	
				<ul>
					<p>Répondre aux défis de l'hôpital moderne :</p>
					<li>Démédicalisation démographique et géographique, afflux inégalé de patients dans les structures d’urgence, investissement nécessaire en personnels médicaux et non-médicaux de qualité, investissement en matériels associé à une restructuration de l’offre de soins, développement de coopérations avec le privé en particulier dans le domaine de la permanence des soins;</li>
					<li>Défense de la DE 2003/88 qui est attaquée, car obstacle à la libéralisation du marché du travail médical et s’intégrer aux combats pour le droit à une santé de qualité pour tous, par des professionnels
			compétents et satisfaits au travail . C’est le sens de notre adhésion à la FEMS.</li>
				</ul>
			<p><span>Dr James Brodeur, Président SMARNU (30 mai 2009)</span></p>
		</article>

