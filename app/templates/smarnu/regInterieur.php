	<h1>Règlement intérieur et statut</h1>

	<section>
		<h2><i class="fa fa-arrow-right" aria-hidden="true"></i> Statut</h2>
		<article>
			<h3>TITRE I : Constitution - Objet</h3>
			
			<p><span>Article 1er -</span> Il est formé entre les susnommés, les délégués-fondateurs précités et tous ceux qui adhéreront
			aux présents statuts, un syndicat professionnel conformément au Code du travail, livre III, titre I, articles 1er
			et 26.</p>
			<p><span>Article 2 -</span> Le syndicat prend la dénomination suivante :
			Syndicat des Médecins Anesthésistes Réanimateurs non Universitaires.</p>
			<p><span>Article 3 -</span> Le siège du syndicat est fixé à l'adresse du secrétaire général
			Docteur Danièle GOUMARD
			77, rue des Gonthières 17140 LAGORD
			et pourra être transféré par simple décision du Conseil d’Administration.
			<p><span>Article 4 -</span> Ce syndicat a exclusivement pour objet l'étude et la défense des intérêts moraux et matériels des
			personnels médicaux anesthésistes réanimateurs titulaires à temps plein et temps partiel des hôpitaux, des
			Assistants spécialistes Anesthésistes Réanimateurs, des Praticiens Contractuels, des Praticiens anesthésistes
			réanimateurs des Etablissements Participants au service Public Hospitalier (PSPH) et des Centres anticancéreux
			régis respectivement par les Décrets :
			n°84131 du 24 février 1984,
			n°85384 du 29 mars 1985,
			n°87788 du 28 septembre 1987,
			n° 93701 du 27 mars 1993
			n°45-2221 du 1ier octobre 1945
			afin de leur permettre d'assurer leurs fonctions.</p>
			<p><span>Article 5 -</span> Pour réaliser son objet, le syndicat pourra :</p>
			<p>a) Constituer un centre d'action pour la défense des intérêts généraux et particuliers de la profession,
			notamment dans ses rapports avec les pouvoirs publics, les administrations publiques ou privées, les autres
			syndicats et organismes économiques, la presse, etc., signaler en conséquence aux pouvoirs publics les
			besoins de la profession, les améliorations qu'elle réclame, et en poursuivre la réalisation.</p>
			<p>b) Encourager, même par des subsides en espèces, toutes institutions ou améliorations intéressant le
			développement et la prospérité de la profession.</p>
			<p>c) Acheter ou louer pour les prêter à ses membres, tous les objets nécessaires à l'exercice du droit syndical.</p>
			<p>d) Constituer un bureau de conciliation et de consultation en vue des affaires contentieuses ou des questions
			sur lesquelles il peut être appelé à statuer ou à donner son avis, soit par les intéressés, soit par les tribunaux,
			soit par les autorités publiques.</p>
			<p><span>Article 6 -</span> Il est interdit au syndicat de s'occuper pour son compte d'entreprises commerciales.</p>
			<p><span>Article 7 -</span> La durée du syndicat n'est pas limitée.</p>
		</article>

		<article>
			<h3>TITRE II : Admissions</h3>
			
			<p><span>Article 8 -</span> Peuvent adhérer au syndicat les membres de la profession désignés par l'article 4 et agréés par le
			Conseil d'Administration. Toute demande d'admission doit être adressée par écrit au Président du Conseil
			d'Administration.</p>
			<p><span>Article 10 -</span> Peuvent continuer à faire partie du syndicat les personnes qui ont quitté l'exercice de la
			profession, du fait de leur mise à leur retraite. Elles ont le titre de membres honoraires.
			Peuvent également être admises comme membres d'honneur les personnes ayant apporté un concours utile à
			la cause du syndicat. La qualité de membre d'honneur est donné par l'Assemblée Générale sur proposition du
			Conseil d'Administration.</p>
			<p><span>Article 11 -</span> Le Conseil d'Administration a tous pouvoirs pour admettre, ajourner ou refuser définitivement
			toute demande d'admission sans qu'il puisse être tenu de faire connaître les motifs de sa décision.
			Si le Conseil d'Administration ajourne ou refuse définitivement une demande d'admission, le litige est porté
			devant l'Assemblée Générale dans sa plus proche réunion. L'Assemblée Générale statue souverainement dans
			les conditions de majorité prévues aux présents statuts.</p>
			<p><span>Article 12 -</span> Toute personne s'engage par ce fait à respecter les statuts et règlements du syndicat.</p>
			<p><span>Article 13 -</span> Chaque membre doit payer au syndicat une cotisation annuelle dont le taux est fixé en
			Assemblée Générale sur la proposition du Conseil d'Administration. Cette cotisation sera due à partir du 1er
			janvier de l'année dans laquelle le syndiqué a commencé à faire partie du syndicat, et payable d'avance.</p>
			<p><span>Article 14 -</span> Perdent la qualité de membre du syndicat :
			- Ceux qui ont donné leur démission par lettre adressée au Président du Conseil
			d'Administration.</p>
			<p>- Ceux dont l'Assemblée Générale a prononcé l'exclusion sur proposition du Conseil
			d'Administration.Le décès, la démission ou l'exclusion d'un membre du syndicat ne met pas fin au syndicat, qui continue
			d'exister entre les autres membres.</p>
		</article>

		<article>
			<h3>TITRE III : Administration</h3>
			
			<p><span>Article 15 -</span> Le syndicat est administré par un Conseil d'Administration comprenant
			20 membres maximum, nommés par l'Assemblée Générale à la majorité absolue des voix des adhérents
			présents ou représentés.
			Les délégués régionaux, sont membres invités permanents du Conseil d'Administration.</p>
			<p><span>Article 16 -</span> Les administrateurs sont élus pour trois ans par l'Assemblée Générale et choisis parmi les
			membres fondateurs et les membres actifs jouissant de leurs droits civils et politiques.</p>
			<p><span>Article 17 -</span> Le premier Conseil restera en fonction jusqu'à la prochaine Assemblée Générale qui le
			renouvellera en entier. A partir de cette Assemblée, le Conseil se renouvelle à l'Assemblée Générale annuelle
			par tiers tous les ans suivant un ordre de sortie déterminé, pour les deux premières fois par un tirage au sort
			parmi les plus anciens, puis, la troisième fois, d'après l'ancienneté de nomination.</p>
			<p><span>Article 18 -</span> Tout membre sortant est rééligible.</p>
			<p><span>Article 19 -</span> Les fonctions de membre de Conseil sont gratuites.</p>
			<p><span>Article 20 -</span> Chaque année, le Conseil élit son bureau dans la première réunion qui suit l'Assemblée Générale
			où les membres du Conseil ont été désignés.</p>
			<p><span>Article 21 -</span> Le bureau est composé de : un président, quatre vice-présidents, un secrétaire général, un
			secrétaire général adjoint, un trésorier, un trésorier adjoint</p>
			<p><span>Article 22 -</span> Si, pour quelque cause que ce soit au cours de leur mandat, le président, le secrétaire ou le
			trésorier sont dans l'impossibilité d'exercer leurs fonctions, ils sont remplacés respectivement par un viceprésident,
			un secrétaire adjoint désignés par le Conseil.</p>
			<p><span>Article 23 -</span> Le conseil fixe lui-même le nombre et la date de ses séances. Le Président fixe l'ordre du jour et
			peut, s'il le juge nécessaire, réunir le Conseil en séance extraordinaire. Le secrétaire général est chargé des
			convocations.</p>
			<p><span>Article 24 -</span> Les réunions sont présidées par le Président ou, à défaut, par un vice-président, qui dirige les
			discussions, surveille et assure l'observation des statuts et règlements intérieurs.</p>
			<p><span>Article 25 -</span> Les délibérations ne sont valables que si la réunion comprend au moins le tiers des membres du
			Conseil. Les résolutions sont prises à la majorité des membres présents. En cas de partage, la voix du
			Président est prépondérante. Les délibérations sont constatées par la signature du président de séance et du
			secrétaire général.</p>
			<p><span>Article 26 -</span> Les pouvoirs d'administration sont confiés au Conseil d'Administration qui peut les déléguer au
			bureau en totalité ou en partie.</p>
			<p><span>Article 27 -</span> Le Conseil est chargé de la gestion des affaires syndicales. Il prend toutes décisions et mesures
			sur les questions intéressant le syndicat, accorde ou refuse au bureau les autorisations dont ce dernier a besoin
			pour agir, lui donne les avis qu'il demande, prononce l'admission des adhérents nouveaux, rédige les
			règlements d'ordre intérieur pour l'exécution des statuts, prépare les propositions à soumettre à l'Assemblée
			Générale.</p>
			<p><span>Article 28 -</span> Le bureau administre le patrimoine constitué dans les termes et les limites de la loi, décide de
			l'emploi et du dépôt des fonds disponibles, dresse le budget, ordonne les dépenses et les recouvrements,
			accepte les dons, legs et subventions, décide les achats et les ventes, nomme et révoque les employés,
			présente chaque année à l'Assemblée Générale un rapport sur l'ensemble des opérations de l'exercice et sur la
			situation financière.</p>
			<p><span>Article 29 -</span> Les membres du bureau ne contractent à raison de leur gestion aucune obligation personnelle ni
			solidaire avec les syndiqués ou les tiers. Ils ne répondent que de l'exécution de leur mandat dans les termes de
			la législation sur les syndicats professionnels et du Code Civil.</p>
			<p><span>Article 30 -</span> Le Président dirige les discussions dans les réunions du bureau, du Conseil, de l'Assemblée
			Générale.
			Il surveille et assure l'observation des statuts et règlements intérieurs.
			Il signe tous actes, toutes mesures ou des extraits des délibérations intéressant le syndicat, vise les pièces de
			dépenses à payer, représente le syndicat vis-à-vis des tiers et de l'autorité publique.
			Le Président représente le syndicat en justice, soit comme demandeur, soit comme défenseur, soit comme
			partie civile.</p>
			<p><span>Article 31 -</span> Le Secrétaire général est dépositaire des registres, états et documents concernant l'administration
			du syndicat : il tient la correspondance et peut la signer par délégation du Président. Il rédige les procèsverbaux
			des séances.</p>
			<p><span>Article 32 -</span> Le trésorier est dépositaire des fonds du syndicat : il recouvre les cotisations et autres créances,
			solde les dépenses sur visa du Président, soumet les états de recettes et de dépenses à la vérification du
			bureau. Il dresse en fin d'année le compte de l'exercice annuel qui sera soumis à l'Assemblée Générale.</p>
			<p><span>Article 33 -</span> Le Conseil peut allouer des frais de bureau à l'un ou plusieurs de ses membres et leur adjoindre
			un ou plusieurs employés.</p>
			<p><span>Article 34 -</span> Le Conseil d'Administration peut admettre à ses séances des adhérents pour y développer les
			propositions qu'ils auraient préalablement soumis par écrit.</p>
			<p><span>Article 35 -</span> Chaque membre du Conseil doit assister en personne aux séances.</p>
			 
		</article>

		<article>
			<h3>TITRE IV : Assemblée Générale</h3>
			
			<p><span>Article 36 -</span> L'Assemblée Générale se compose de tous les adhérents du syndicat. Elle se réunit en séance
			ordinaire une fois par an, au jour fixé par le bureau et sur convocation du secrétaire général. Il pourra être
			tenu des assemblées extraordinaires quand les intérêts du syndicat l'exigeront, soit sur l'avis du Conseil
			d'Administration, soit sur une demande signée du quart des adhérents inscrits.</p>
			<p><span>Article 37 -</span> Les convocations pour l'Assemblée Générale ordinaire seront adressées au moins un mois à
			l'avance par lettre individuelle (ou par la voie du journal, organe officiel du syndicat) et porteront indication
			des questions à l'ordre du jour.</p>
			<p><span>Article 38 -</span> Tout adhérent au syndicat a le droit de se faire représenter à l'Assemblée par un autre adhérent,
			en remettant à ce dernier un mandat écrit. Le nombre de pouvoirs est limité à 10 par mandataire.</p>
			<p><span>Article 39 -</span> Le bureau de l'Assemblée Générale est le même que le bureau du Conseil d'Administration.</p>
			<p><span>Article 40 -</span> L'Assemblée Générale est l'organe souverain du syndicat, seuls les membres actifs y ont droit
			de vote.</p>
			<p><span>Article 41 -</span> L’Assemblée Générale a pouvoir pour nommer et révoquer les membres du Conseil
			d'Administration, pour approuver le rapport annuel de gestion et les rapports particuliers sur l'activité des
			administrateurs. Elle donne ses directives pour l'exercice à venir.</p>
			<p><span>Article 42 -</span> Les décisions de l'Assemblée Générale relatives à tout autre objet que la modification des statuts,
			seront prises à la majorité des membres présents ou représentés.</p>
			<p><span>Article 43 -</span> L'Assemblée Générale vote à main levée, à moins que le scrutin secret ait été demandé par le Président
			ou par dix membres au moins.</p>
			<p><span>Article 44 -</span> Il sera fait un rapport annuel de gestion à l'Assemblée Générale ordinaire.
			Ce rapport exposera les travaux de bureau pendant l'exercice écoulé, les changements survenus dans la
			situation des adhérents, la situation financière et le bilan.</p>
			<p><span>Article 45 -</span> L'Assemblée Générale a le pouvoir de modifier les statuts.</p>
			<p><span>Article 46 -</span> Aucune proposition de modification des statuts ne peut venir en discussion à l'Assemblée
			Générale si elle n'a été précédée d'une délibération du Conseil d'Administration qui devra présenter un
			rapport motivé ou proposé par écrit par le quart au moins des membres actifs du syndicat.</p>
			<p><span>Article 47 -</span> Les décisions de l'Assemblée Générale relatives à la modification des statuts seront prises à la
			majorité des membres actifs inscrits au syndicat. Si, sur une première convocation, l'Assemblée n'a pas pu
			réunir ce nombre des membres actifs, il peut être convoqué, à un mois au moins d'intervalle, une deuxième
			Assemblée qui délibère valablement, quel que soit le nombre des sociétaires présents ou représentés, mais
			seulement à la majorité des deux tiers des voix des membres présents.</p>
			<p><span>Article 48 -</span> L'Assemblée Générale peut prononcer la dissolution du syndicat suivant
			les conditions prévues au titre VI des présents statuts.</p>
		</article>
		<article>
			<h3>TITRE V : Pouvoir disciplinaire</h3>
			
			<p><span>Article 49 -</span> L'exclusion peut être prononcée à l'égard de tout membre qui, par ses agissements, porterait un
			préjudice matériel ou moral au syndicat.</p>
			<p><span>Article 50 -</span> L'exclusion peut être temporaire ou définitive. Le Conseil d’Administration peut prononcer
			l’exclusion temporaire des membres qui commettraient des manquements graves aux statuts ou au règlement
			intérieur, des membres qui refuseraient de payer leurs cotisations, sans préjudice du droit pour le syndicat
			d’en poursuivre le recouvrement.
			L'Assemblée Générale est seule habilitée à prononcer l'exclusion définitive d'un membre du syndicat.</p>
			<p><span>Article 51 -</span> Le membre du syndicat poursuivi disciplinairement devant le Conseil d’Administration doit être
			averti par lettres des poursuites intentées contre lui et admis à fournir toutes explications orales ou écrites.</p>
			<p><span>Article 52 -</span> Tout adhérent exclu perd ses droits sur la cotisation versée au syndicat pour l'année en cours.</p>
		</article>
		<article>
			<h3>TITRE VI : Dissolution - Liquidation</h3>
			
			<p><span>Article 53 -</span> Le syndicat peut être dissous sur la proposition du bureau, par un vote de l'Assemblée Générale
			pris à la majorité des deux tiers des membres présents ou représentés.</p>
			<p><span>Article 54 -</span> En cas de dissolution volontaire, statutaire ou prononcée par justice, l'Assemblée Générale
			déterminera l'emploi de l'actif net.
			En aucun cas, les biens ne peuvent être répartis entre les membres adhérents.
			Elle attribuera l'actif net à une ou plusieurs organisations défendant les intérêts des médecins
			anesthésistes, dans des conditions fixées par elle.</p>
			<p><span>Article 55 -</span> Le bureau sera chargé de procéder à la liquidation des biens du syndicat conformément aux
			dispositions des statuts ou aux décisions de l'Assemblée Générale.
			Les présents statuts seront déposés conformément à la loi. </p>
		</article>
	
		<h2><i class="fa fa-arrow-right" aria-hidden="true"></i> Réglement Intérieur</h2>
		<article>
			<h3>Règles de fonctionnement du Conseil d'Administration et du Bureau</h3>
			<p><span>Article 1 : Elections du Conseil d’administration</span></p>
			<p>Les administrateurs sont élus pour trois ans par l'Assemblée Générale et choisis parmi les membres
			fondateurs et les membres actifs jouissant de leurs droits civils et politiques. ( Article 16 des statuts)</p>
			<p>Les administrateurs qui ont quitté l'exercice de la profession, du fait de leur mise à leur retraite
			peuvent continuer à faire partie du syndicat sous le titre de membres honoraires. (Article 10 )
			Le Secrétaire Général procède à la déclaration de vacance de poste et à l'appel de candidatures, adressés à
			tous les membres du Syndicat par la convocation à l’Assemblée Générale.</p>
			<p>Après cet appel, les candidatures doivent être adressées au Secrétaire Général .
			Sur leur demande, les administrateurs peuvent être renouvelés dans leur fonction sans limitation du nombre
			de fois.
			Il est procédé à l’élection des candidats lors de l’Assemblée Générale annuelle.</p>
			<p>Les votes ont lieu à main levée ou à scrutin secret à la demande d’un des présents à l’Assemblée Générale.
			Si plusieurs candidats obtiennent le même nombre de suffrages alors qu'il ne reste plus qu'un seul poste à
			pourvoir, le candidat le plus âgé est déclaré élu.
			Le nouveau Conseil d’Administration entre en fonction à l'issue de l'Assemblée Générale au cours de
			laquelle sont proclamés les résultats des élections.</p>
		</article>

		<article>
			<p><span>Article 2 : Rôle et missions du Conseil d’Administration</span></p>
			<p><span>Article 27 -</span> Le Conseil est chargé de la gestion des affaires syndicales. Il prend toutes décisions et mesures sur les
			questions intéressant le syndicat, accorde ou refuse au bureau les autorisations dont ce dernier a besoin pour agir, lui
			donne les avis qu'il demande, prononce l'admission des adhérents nouveaux, rédige les règlements d'ordre intérieur
			pour l'exécution des statuts, prépare les propositions à soumettre à l'Assemblée Générale.</p>
			<p>Tous les membres du Conseil d’Administration sont requis de participer nominativement à un groupe de
			travail 
			Le Conseil d’Administration se réunit au moins 4 fois par an . Les réunions peuvent avoir lieu en réunion
			téléphonique, mais une fois par an au minimum les membres du conseil doivent se retrouver sur un même
			lieu.</p>
			<p>Ces réunions se font sur convocation du Secrétaire Général par courrier électronique.
			La réponse de présence est indispensable particulièrement pour la réservation des lignes téléphoniques.
			L’ordre du jour est donné par le Président.</p>
		</article>

		<article>
			<p><span>Article 3 - </span>Le Conseil d’Administration peut créer des commissions ou groupes de travail dont il fixe le nombre, la ou les missions, et la composition.
			Il est fait appel à des membres extérieurs au Conseil d’Administration pour participer aux travaux
			des Commissions.</p>
			<p>Leurs conditions de fonctionnement sont définies par le Conseil d’Administration.</p>
			<p>- Commission FEMS relations Européenne</p>
			<p>- Commission droits et retraite</p>
			<p>- Commission fonctionnement du syndicat et délégués régionaux</p>
			<p>- Commission Communication</p>
			<p>Chaque vice président est responsable d’une commission.
			Le président est membre de droit de toutes les commissions.
			D’autres commissions ou groupes de travail peuvent être crées en fonction de l’évolution et des
			évènements</p>
			<p>Il pourra être élaboré pour certaines commissions des règles de fonctionnement.
			Ces commissions se réunissent en réunion téléphonique, en général, sur proposition du responsable
			de la commission .
			Le secrétaire général se charge des convocations.
			Les procès verbaux des réunions seront adressés au secrétaire général qui en fera la diffusion à
			l’ensemble des membres du Conseil d’administration.</p>	
		</article>
		
		<article>
			<p><span>Article 4 - </span>
			La Commission des délégués régionaux organise une réunion annuelle des délégués régionaux, qui se tient le
			même jour que de la réunion annuelle plénière du Conseil d’administration, et à laquelle ils sont sollicités de
			participer.</p>
		</article>

		<article>
			<p><span>Article 5 : Composition du Bureau</span></p>
			<p>Le Bureau est composé de :</p>
			<p>	- un président</p>
			<p>	- quatre vice présidents</p>
			<p>	- un secrétaire général</p>
			<p>	- un secrétaire général adjoint</p>
			<p>	- un trésorier</p>
			<p>	- un trésorier adjoint</p>
		</article>

		<article>
			<p><span>Article 6 : Élections du Bureau</span></p>
			<p>L'élection du Bureau a lieu, au scrutin secret majoritaire à deux tours, lors de la première réunion
			du Conseil d’administration qui suit l'Assemblée Générale. En cas de partage des voix à l'issue du
			deuxième tour, le candidat le plus âgé est déclaré élu.
			Le nouveau Bureau entre en fonction dès son élection.
			Les membres du bureau élisent le Président parmi eux.
			Son mandat est de 1 an renouvelable 3 fois.
			La répartition des postes se fait à l’intérieur du bureau élu.</p>
			<p>Afin de garder une mémoire dans le bureau, l’ensemble des membres ne peut être renouvelé en totalité.</p>
		</article>
		
		<article>
			<p><span>Article 7 : Rôle du Bureau </span></p>
			<p><span>Article 28 -</span> Le bureau administre le patrimoine constitué dans les termes et les limites de la loi, décide de
			l'emploi et du dépôt des fonds disponibles, dresse le budget, ordonne les dépenses et les recouvrements, accepte les
			dons, legs et subventions, décide les achats et les ventes, nomme et révoque les employés, présente chaque année à
			l'Assemblée Générale un rapport sur l'ensemble des opérations de l'exercice et sur la situation financière.</p>
		</article>
		
		<article>
			<h3>Attribution du bureau</h3>
			<p><span>Article 8 : Attribution du Président et des Vice-Présidents</span></p>
			<p>Le Président règle l'ordre du jour des réunions, des Assemblées Générales Ordinaires et
			Extraordinaires, du Conseil d’administration et du Bureau, en liaison avec le Secrétaire Général qui
			l'adresse aux membres concernés.
			Il préside les séances et appelle les sujets à traiter conformément à l'ordre du jour. Il ne peut modifier
			l'ordre dans lequel ces sujets sont traités sans l'accord de la majorité des participants ayant voix
			délibérative, à la réunion concernée.</p>
			<p>Il dirige les discussions, met aux voix les propositions, recueille les suffrages et proclame les
			décisions du syndicat
			Il veille à la régularité des listes de présence.
			En cas d'absence, le Président est remplacé par le Vice-Président.
			Le président est membre de droit, vice président de la Confédération des Praticiens Hospitaliers.
