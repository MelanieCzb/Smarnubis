	<h2>Règlement intérieur et statut</h2>

	<section>
		<p>Statut</p>
		<article>
			TITRE I
			CONSTITUTION - OBJET
			Article 1er - Il est formé entre les susnommés, les délégués-fondateurs précités et tous ceux qui adhéreront
			aux présents statuts, un syndicat professionnel conformément au Code du travail, livre III, titre I, articles 1er
			et 26.
			Article 2 - Le syndicat prend la dénomination suivante :
			Syndicat des Médecins Anesthésistes Réanimateurs non Universitaires.
			Article 3 - Le siège du syndicat est fixé à l'adresse du secrétaire général
			Docteur Danièle GOUMARD
			77, rue des Gonthières 17140 LAGORD
			et pourra être transféré par simple décision du Conseil d’Administration.
			Article 4 - Ce syndicat a exclusivement pour objet l'étude et la défense des intérêts moraux et matériels des
			personnels médicaux anesthésistes réanimateurs titulaires à temps plein et temps partiel des hôpitaux, des
			Assistants spécialistes Anesthésistes Réanimateurs, des Praticiens Contractuels, des Praticiens anesthésistes
			réanimateurs des Etablissements Participants au service Public Hospitalier (PSPH) et des Centres anticancéreux
			régis respectivement par les Décrets :
			n°84131 du 24 février 1984,
			n°85384 du 29 mars 1985,
			n°87788 du 28 septembre 1987,
			n° 93701 du 27 mars 1993
			n°45-2221 du 1ier octobre 1945
			afin de leur permettre d'assurer leurs fonctions.
			Article 5 - Pour réaliser son objet, le syndicat pourra :
			a) Constituer un centre d'action pour la défense des intérêts généraux et particuliers de la profession,
			notamment dans ses rapports avec les pouvoirs publics, les administrations publiques ou privées, les autres
			syndicats et organismes économiques, la presse, etc., signaler en conséquence aux pouvoirs publics les
			besoins de la profession, les améliorations qu'elle réclame, et en poursuivre la réalisation.
			b) Encourager, même par des subsides en espèces, toutes institutions ou améliorations intéressant le
			développement et la prospérité de la profession.
			c) Acheter ou louer pour les prêter à ses membres, tous les objets nécessaires à l'exercice du droit syndical.
			d) Constituer un bureau de conciliation et de consultation en vue des affaires contentieuses ou des questions
			sur lesquelles il peut être appelé à statuer ou à donner son avis, soit par les intéressés, soit par les tribunaux,
			soit par les autorités publiques.
			Article 6 - Il est interdit au syndicat de s'occuper pour son compte d'entreprises commerciales.
			Article 7 - La durée du syndicat n'est pas limitée. 
		</article>

		<article>
			TITRE II
			ADMISSIONS
			Article 8 - Peuvent adhérer au syndicat les membres de la profession désignés par l'article 4 et agréés par le
			Conseil d'Administration. Toute demande d'admission doit être adressée par écrit au Président du Conseil
			d'Administration.
			Article 10 - Peuvent continuer à faire partie du syndicat les personnes qui ont quitté l'exercice de la
			profession, du fait de leur mise à leur retraite. Elles ont le titre de membres honoraires.
			Peuvent également être admises comme membres d'honneur les personnes ayant apporté un concours utile à
			la cause du syndicat. La qualité de membre d'honneur est donné par l'Assemblée Générale sur proposition du
			Conseil d'Administration.
			Article 11 - Le Conseil d'Administration a tous pouvoirs pour admettre, ajourner ou refuser définitivement
			toute demande d'admission sans qu'il puisse être tenu de faire connaître les motifs de sa décision.
			Si le Conseil d'Administration ajourne ou refuse définitivement une demande d'admission, le litige est porté
			devant l'Assemblée Générale dans sa plus proche réunion. L'Assemblée Générale statue souverainement dans
			les conditions de majorité prévues aux présents statuts.
			Article 12 - Toute personne s'engage par ce fait à respecter les statuts et règlements du syndicat.
			Article 13 - Chaque membre doit payer au syndicat une cotisation annuelle dont le taux est fixé en
			Assemblée Générale sur la proposition du Conseil d'Administration. Cette cotisation sera due à partir du 1er
			janvier de l'année dans laquelle le syndiqué a commencé à faire partie du syndicat, et payable d'avance.

			Article 14 - Perdent la qualité de membre du syndicat :
			- Ceux qui ont donné leur démission par lettre adressée au Président du Conseil
			d'Administration.
			- Ceux dont l'Assemblée Générale a prononcé l'exclusion sur proposition du Conseil
			d'Administration.
			Le décès, la démission ou l'exclusion d'un membre du syndicat ne met pas fin au syndicat, qui continue
			d'exister entre les autres membres.
		</article>

		<article>
			TITRE III
			ADMINISTRATION
			Article 15 - Le syndicat est administré par un Conseil d'Administration comprenant
			20 membres maximum, nommés par l'Assemblée Générale à la majorité absolue des voix des adhérents
			présents ou représentés.
			Les délégués régionaux, sont membres invités permanents du Conseil d'Administration.
			Article 16 - Les administrateurs sont élus pour trois ans par l'Assemblée Générale et choisis parmi les
			membres fondateurs et les membres actifs jouissant de leurs droits civils et politiques.
			Article 17 - Le premier Conseil restera en fonction jusqu'à la prochaine Assemblée Générale qui le
			renouvellera en entier. A partir de cette Assemblée, le Conseil se renouvelle à l'Assemblée Générale annuelle
			par tiers tous les ans suivant un ordre de sortie déterminé, pour les deux premières fois par un tirage au sort
			parmi les plus anciens, puis, la troisième fois, d'après l'ancienneté de nomination.
			Article 18 - Tout membre sortant est rééligible.
			Article 19 - Les fonctions de membre de Conseil sont gratuites.
			Article 20 - Chaque année, le Conseil élit son bureau dans la première réunion qui suit l'Assemblée Générale
			où les membres du Conseil ont été désignés.
			Article 21 - Le bureau est composé de : un président, quatre vice-présidents, un secrétaire général, un
			secrétaire général adjoint, un trésorier, un trésorier adjoint
			Article 22 - Si, pour quelque cause que ce soit au cours de leur mandat, le président, le secrétaire ou le
			trésorier sont dans l'impossibilité d'exercer leurs fonctions, ils sont remplacés respectivement par un viceprésident,
			un secrétaire adjoint désignés par le Conseil.
			Article 23 - Le conseil fixe lui-même le nombre et la date de ses séances. Le Président fixe l'ordre du jour et
			peut, s'il le juge nécessaire, réunir le Conseil en séance extraordinaire. Le secrétaire général est chargé des
			convocations.
			Article 24 - Les réunions sont présidées par le Président ou, à défaut, par un vice-président, qui dirige les
			discussions, surveille et assure l'observation des statuts et règlements intérieurs.
			Article 25 - Les délibérations ne sont valables que si la réunion comprend au moins le tiers des membres du
			Conseil. Les résolutions sont prises à la majorité des membres présents. En cas de partage, la voix du
			Président est prépondérante. Les délibérations sont constatées par la signature du président de séance et du
			secrétaire général.
			Article 26 - Les pouvoirs d'administration sont confiés au Conseil d'Administration qui peut les déléguer au
			bureau en totalité ou en partie.
			Article 27 - Le Conseil est chargé de la gestion des affaires syndicales. Il prend toutes décisions et mesures
			sur les questions intéressant le syndicat, accorde ou refuse au bureau les autorisations dont ce dernier a besoin
			pour agir, lui donne les avis qu'il demande, prononce l'admission des adhérents nouveaux, rédige les
			règlements d'ordre intérieur pour l'exécution des statuts, prépare les propositions à soumettre à l'Assemblée
			Générale.
			Article 28 - Le bureau administre le patrimoine constitué dans les termes et les limites de la loi, décide de
			l'emploi et du dépôt des fonds disponibles, dresse le budget, ordonne les dépenses et les recouvrements,
			accepte les dons, legs et subventions, décide les achats et les ventes, nomme et révoque les employés,
			présente chaque année à l'Assemblée Générale un rapport sur l'ensemble des opérations de l'exercice et sur la
			situation financière.
			Article 29 - Les membres du bureau ne contractent à raison de leur gestion aucune obligation personnelle ni
			solidaire avec les syndiqués ou les tiers. Ils ne répondent que de l'exécution de leur mandat dans les termes de
			la législation sur les syndicats professionnels et du Code Civil.
			Article 30 - Le Président dirige les discussions dans les réunions du bureau, du Conseil, de l'Assemblée
			Générale.
			Il surveille et assure l'observation des statuts et règlements intérieurs.
			Il signe tous actes, toutes mesures ou des extraits des délibérations intéressant le syndicat, vise les pièces de
			dépenses à payer, représente le syndicat vis-à-vis des tiers et de l'autorité publique.
			Le Président représente le syndicat en justice, soit comme demandeur, soit comme défenseur, soit comme
			partie civile.
			Article 31 - Le Secrétaire général est dépositaire des registres, états et documents concernant l'administration
			du syndicat : il tient la correspondance et peut la signer par délégation du Président. Il rédige les procèsverbaux
			des séances.
			Article 32 - Le trésorier est dépositaire des fonds du syndicat : il recouvre les cotisations et autres créances,
			solde les dépenses sur visa du Président, soumet les états de recettes et de dépenses à la vérification du
			bureau. Il dresse en fin d'année le compte de l'exercice annuel qui sera soumis à l'Assemblée Générale.
			Article 33 - Le Conseil peut allouer des frais de bureau à l'un ou plusieurs de ses membres et leur adjoindre
			un ou plusieurs employés.
			Article 34 - Le Conseil d'Administration peut admettre à ses séances des adhérents pour y développer les
			propositions qu'ils auraient préalablement soumis par écrit.
			Article 35 - Chaque membre du Conseil doit assister en personne aux séances. 
		</article>

		<article>
			TITRE IV
			ASSEMBLEE GENERALE
			Article 36 - L'Assemblée Générale se compose de tous les adhérents du syndicat. Elle se réunit en séance
			ordinaire une fois par an, au jour fixé par le bureau et sur convocation du secrétaire général. Il pourra être
			tenu des assemblées extraordinaires quand les intérêts du syndicat l'exigeront, soit sur l'avis du Conseil
			d'Administration, soit sur une demande signée du quart des adhérents inscrits.
			Article 37 - Les convocations pour l'Assemblée Générale ordinaire seront adressées au moins un mois à
			l'avance par lettre individuelle (ou par la voie du journal, organe officiel du syndicat) et porteront indication
			des questions à l'ordre du jour.
			Article 38 - Tout adhérent au syndicat a le droit de se faire représenter à l'Assemblée par un autre adhérent,
			en remettant à ce dernier un mandat écrit. Le nombre de pouvoirs est limité à 10 par mandataire.
			Article 39 - Le bureau de l'Assemblée Générale est le même que le bureau du Conseil d'Administration.
			Article 40 - L'Assemblée Générale est l'organe souverain du syndicat, seuls les membres actifs y ont droit
			de vote.
			Article 41 - L’Assemblée Générale a pouvoir pour nommer et révoquer les membres du Conseil
			d'Administration, pour approuver le rapport annuel de gestion et les rapports particuliers sur l'activité des
			administrateurs. Elle donne ses directives pour l'exercice à venir.
			Article 42 - Les décisions de l'Assemblée Générale relatives à tout autre objet que la modification des statuts,
			seront prises à la majorité des membres présents ou représentés.
			Article 43 - L'Assemblée Générale vote à main levée, à moins que le scrutin secret ait été demandé par le Président
			ou par dix membres au moins.
			Article 44 - Il sera fait un rapport annuel de gestion à l'Assemblée Générale ordinaire.
			Ce rapport exposera les travaux de bureau pendant l'exercice écoulé, les changements survenus dans la
			situation des adhérents, la situation financière et le bilan.
			Article 45 - L'Assemblée Générale a le pouvoir de modifier les statuts.
			Article 46 - Aucune proposition de modification des statuts ne peut venir en discussion à l'Assemblée
			Générale si elle n'a été précédée d'une délibération du Conseil d'Administration qui devra présenter un
			rapport motivé ou proposé par écrit par le quart au moins des membres actifs du syndicat.
			Article 47 - Les décisions de l'Assemblée Générale relatives à la modification des statuts seront prises à la
			majorité des membres actifs inscrits au syndicat. Si, sur une première convocation, l'Assemblée n'a pas pu
			réunir ce nombre des membres actifs, il peut être convoqué, à un mois au moins d'intervalle, une deuxième
			Assemblée qui délibère valablement, quel que soit le nombre des sociétaires présents ou représentés, mais
			seulement à la majorité des deux tiers des voix des membres présents.
			Article 48 - L'Assemblée Générale peut prononcer la dissolution du syndicat suivant
			les conditions prévues au titre VI des présents statuts.
		</article>
		<article>
			TITRE V
			POUVOIR DISCIPLINAIRE
			Article 49 - L'exclusion peut être prononcée à l'égard de tout membre qui, par ses agissements, porterait un
			préjudice matériel ou moral au syndicat.
			Article 50 - L'exclusion peut être temporaire ou définitive. Le Conseil d’Administration peut prononcer
			l’exclusion temporaire des membres qui commettraient des manquements graves aux statuts ou au règlement
			intérieur, des membres qui refuseraient de payer leurs cotisations, sans préjudice du droit pour le syndicat
			d’en poursuivre le recouvrement.
			L'Assemblée Générale est seule habilitée à prononcer l'exclusion définitive d'un membre du syndicat.
			Article 51 - Le membre du syndicat poursuivi disciplinairement devant le Conseil d’Administration doit être
			averti par lettres des poursuites intentées contre lui et admis à fournir toutes explications orales ou écrites.
			Article 52 - Tout adhérent exclu perd ses droits sur la cotisation versée au syndicat pour l'année en cours.
		</article>
		<article>
			TITRE VI
			DISSOLUTION - LIQUIDATION
			Article 53 - Le syndicat peut être dissous sur la proposition du bureau, par un vote de l'Assemblée Générale
			pris à la majorité des deux tiers des membres présents ou représentés.
			Article 54 - En cas de dissolution volontaire, statutaire ou prononcée par justice, l'Assemblée Générale
			déterminera l'emploi de l'actif net.
			En aucun cas, les biens ne peuvent être répartis entre les membres adhérents.
			Elle attribuera l'actif net à une ou plusieurs organisations défendant les intérêts des médecins
			anesthésistes, dans des conditions fixées par elle.
			Article 55 - Le bureau sera chargé de procéder à la liquidation des biens du syndicat conformément aux
			dispositions des statuts ou aux décisions de l'Assemblée Générale.
			Les présents statuts seront déposés conformément à la loi. 
		</article>

		
	</section>
